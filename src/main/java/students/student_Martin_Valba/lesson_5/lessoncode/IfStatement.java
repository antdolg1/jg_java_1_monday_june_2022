public class IfStatement {
    public static void main(String[] args) {

        int numberA = 20;
        int numberB = 30;
        if (numberA == numberB) {
            System.out.println("Number are equal");
        } else {
            System.out.println("NOTHING");
        }
        if (numberA > 10) {
            System.out.println("Number A > 10");
        } else {
            System.out.println("NOTHING");
        }
        if (numberB > 10) {
            System.out.println("Number B > 10");
        } else {
            System.out.println("NOTHING");
        }

    }
}





package main.java.students.student_Nikita_Bondars.LessonWork2;

public class Variables {
    public static void main(String[] args) {

        byte myByte = 8;
        System.out.println("myByte value is: " + myByte);

        short myShort = 100;
        System.out.println("myShort value is: " + myShort);

        int myInt = 32000;
        System.out.println("myInt value is: " + myInt);

        long myLong = 1_000_000;
        System.out.println("myLong value is: " + myLong);

        float myFloat = 3.123456789F; // Либо маленькая буква f
        System.out.println("myFloat value is: " + myFloat);

        double myDouble = 3.123456789;
        System.out.println("myDouble value is: " + myDouble);

        boolean myBoolean = true; // Либо false
        System.out.println("myBoolean value is: " + myBoolean);

        char myChar = 'B';
        System.out.println("myChar value is: " + myChar);

    }
}

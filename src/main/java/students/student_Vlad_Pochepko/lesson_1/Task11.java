public class Task11 {
    public static void main(final String[] args) {

        final int num1 = 74;
        final int num2 = 36;
        final int result1 = num1 + num2;

        System.out.println(num1 + " + " + num2);

        System.out.println("Result = " + result1);

        System.out.println();

        final int num3 = 50;
        final int num4 = 3;
        final int result2 = num3 / num4;

        System.out.println(num3 + " / " + num4);

        System.out.println("Result = " + result2);

        //Название класса и переменных не соответствует Java конвенции.
        //В названиях класса мы не используем snake_case, а используем PascalCase. А в названиях переменных мы используем
        //camelCase (об этом мы поговорим на следующем занятии). Плюс есть некоторые замечания по поводу форматирования (это тоже обсудим).
    }
}

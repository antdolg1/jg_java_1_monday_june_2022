package main.java.students.student_maksim_turcin.lessonExercises;

public class PrimitiveVariables {
    public static void main(String[] args) {

        byte myByte = 4;
        System.out.println("myByte result is: " + myByte);

        short myShort = 162;
        System.out.println("myShort result is: " + myShort);

        int myInt = 32000;
        System.out.println("myInt result is: " + myInt);

        long myLong = 12345677;
        System.out.println("myLong: " + myLong);

        float myFloat = 3.14f;
        System.out.println("myFloat: " + myFloat);

        double myDouble = 3.54545;
        System.out.println("myDouble: " + myDouble);

        boolean approve = true;
        System.out.println("boolean:" + approve);

        char myChar = 'B';
        System.out.println("myChar result is: " + myChar);

        System.out.println("Very " + "hot " + "day ");

        System.out.println(7 + "+" + 3 + "=" + (7 + 3));

    }
}

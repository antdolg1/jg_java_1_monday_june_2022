public class Computer {
    String manufacturer;
    String model;

    Computer(final String manufacturer, final String model) {
        this.manufacturer = manufacturer;
        this.model = model;
    }

    String getManufacturer() {
        return manufacturer;
    }

    String getModel() {
        return model;
    }
}
